/* @brief	Main function
 * @details	This is a main entry function for firmware application
 */
int main(void)
{
	/* Initialize the AD7768-1 IIO interface */
	if (ad77681_iio_initialize()) {
		printf("IIO initialization failure!!\r\n");
	}

	while (1) {
		/* Monitor the IIO client events */
		ad77681_iio_event_handler();
	}
}
